package backjoon.bronze.lv5;

import java.util.Scanner;

public class b10869 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);

        int a = scanner.nextInt();
        int b = scanner.nextInt();

        //A+B
        System.out.println(a+b);
        //A-B
        System.out.println(a-b);
        //A*B
        System.out.println(a*b);
        //A/B
        System.out.println(a/b);
        //A%B
        System.out.println(a%b);
    }
}
